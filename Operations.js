/**
 * Basic JS problem 
 * where you implement main mathematical operations
 * through JS functions
 * 
 */

function add(n1, n2) {
    return n1 + n2
}
function substract(n1, n2) {
    return n1 - n2
}
function multiply(n1, n2) {
    return n1 * n2
}
function divide(n1, n2) {
    return n1 / n2
}


